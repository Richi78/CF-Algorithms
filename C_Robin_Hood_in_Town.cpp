void solve(){
    int n; cin >> n;
    vector<int> a(n);
    for(int i=0 ; i<n ; i++) cin >> a[i];
    if(n==1 || n ==2){cout<<-1<<endl; return;}
    sort(all(a));
    int sum=accumulate(all(a),0LL);
    int x = 2 * n * a[n/2] - sum + 1;
    cout<< max(0LL,x) <<endl;
}

