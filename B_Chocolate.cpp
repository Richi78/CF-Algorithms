void solve(){
    int n; cin >> n;
    vector<int> a(n);
    stack<int> st;
    for(int i=0 ; i<n ; i++){
        cin >> a[i];
        if(a[i]) st.push(i);
    } 
    if(st.empty()){
        cout<<"0\n"; return;
    }
    
    int ans=1;
    int idx=st.top();
    st.pop();
    while(!st.empty()){
        int tmp=st.top();
        st.pop();
        ans*=(idx-tmp);
        idx=tmp;
    }
    cout<< ans <<"\n";
}

