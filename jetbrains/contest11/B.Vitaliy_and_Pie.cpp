void solve(){
    int n; cin >> n;
    string s; cin >> s;
    map<char,int> mp;
    int ans=0;
    for(int i=0 ; i<2*n-2 ; i++){
        if(i&1){ // door
            if(mp[tolower(s[i])] > 0){
                mp[tolower(s[i])]--;
            }else ans++;
        }else{ // key
            mp[s[i]]++; 
        }
    }
    cout<< ans <<"\n";
}

